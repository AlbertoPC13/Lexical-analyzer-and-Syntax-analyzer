    private void AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AceptarActionPerformed
        // TODO add your handling code here:
        int a1 = Integer.parseInt((String) AFN1.getSelectedItem());
        String nombre = Nombre.getText();
        int ID = Integer.parseInt(ID_AFD.getText());

        AFN aux1 = new AFN();

        for (AFN a : AFN.ConjDeAFNs) {
            if (a.IdAFN == a1) {
                aux1 = a;
            }
        }

        AFD x = new AFD();
        x = aux1.ConvAFNaAFD();

        if (x != null) {
            x.IdAFD = ID;
            x.GuardarAFN(nombre);
            AFD.ConjAFDs.add(x);
            DefaultTableModel model = (DefaultTableModel) Tabla.getModel();
            Object rows[] = new Object[257];

            for (int i = 0; i < x.NumEstados; i++) {
                for (int j = 0; j < 257; j++) {
                    rows[j] = x.TablaAFD[i][j];
                }
                model.addRow(rows);
            }
            JOptionPane.showMessageDialog(null, "Se ha creado correctamente al AFD");
        } else {
            JOptionPane.showMessageDialog(null, "La operaciÃ³n no ha sido realizada");
            super.dispose();
        }
    }//GEN-LAST:event_AceptarActionPerformed


