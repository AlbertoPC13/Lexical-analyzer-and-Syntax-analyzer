        int a1 = Integer.parseInt((String) AFN1.getSelectedItem());

        AFN aux1 = new AFN();

        for (AFN a : AFN.ConjDeAFNs) {
            if (a.IdAFN == a1)
                aux1 = a;
        }
        
        if (aux1.CerrPos() != null) {
            JOptionPane.showMessageDialog(null, "La cerradura positiva se ha aplicado correctamente al AFN");
            super.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "La operaci√≥n no ha sido realizada");
            super.dispose();
        }

